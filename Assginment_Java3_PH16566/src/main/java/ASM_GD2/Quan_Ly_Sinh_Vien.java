    public void loadData() {
        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            Connection con = DriverManager.getConnection(url, userName, passWord);
            PreparedStatement ps = con.prepareStatement("Select * from STUDENTS");
            ResultSet rs = ps.executeQuery();
            tblmodel.setRowCount(0);
            Vector vector = null;
            while (rs.next()) {
                vector = new Vector();
                vector.add(rs.getString("MaSV"));
                vector.add(rs.getString("HoTen"));
                vector.add(rs.getString("Email"));
                vector.add(rs.getString("SoDT"));
                vector.add(rs.getBoolean("GioiTinh"));
                vector.add(rs.getString("DiaChi"));
                vector.add(rs.getString("Hinh"));
                tblmodel.addRow(vector);
            }
            tblBang.setModel(tblmodel);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void delete() {
        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            Connection con = DriverManager.getConnection(url, userName, passWord);
            PreparedStatement ps = con.prepareStatement("delete from STUDENTS where masv = ?");
            ps.setString(1, txtMaSV.getText());
            int kq = ps.executeUpdate();
            if (kq == 1) {
                JOptionPane.showMessageDialog(this, "Xóa thành công");
            } else {
                JOptionPane.showMessageDialog(this, "Xóa thất bại");
            }
            ps.close();
            con.close();
            this.loadData();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

