    public void update() {
        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            Connection con = DriverManager.getConnection(url, userName, passWord);
            PreparedStatement ps = con.prepareStatement("update STUDENTS set HoTen = ? , Email = ? , SoDT = ? , GioiTinh = ? , DiaChi = ? , Hinh = ? where MaSV = ?   ");
            ps.setString(1, txtHoTen.getText());
            ps.setString(2, txtEmail.getText());
            ps.setString(3, txtSoDT.getText());
            ps.setBoolean(4, (rdoNam.isSelected() == true) ? true : false);
            ps.setString(5, txaDiaChi.getText());
            ps.setString(6, lblHinh.getText());
            ps.setString(7, txtMaSV.getText());
            int kq = ps.executeUpdate();
            if (kq == 1) {
                JOptionPane.showMessageDialog(this, "Update thành công");
            } else {
                JOptionPane.showMessageDialog(this, "Update thất bại");
            }
            ps.close();
            con.close();
            this.loadData();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

